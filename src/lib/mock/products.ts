function makeVariants(
  sizes: string[],
  colors: { name: string; hex: string }[],
  prefix: string,
): Product['variants'] {
  return sizes.flatMap((size, si) =>
    colors.map((color, ci) => ({
      id: `${prefix}-${si}-${ci}`,
      size,
      color: color.name,
      colorHex: color.hex,
      stock: Math.floor(Math.random() * 20) + 1,
      sku: `${prefix}-${size}-${color.name}`.toUpperCase().replace(/\s/g, '-'),
    })),
  )
}

